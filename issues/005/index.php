<h5>Curated related word outliers from various dictionary sources</h5>

<p>Even through we are following a scientific process, it's easy to be dismissive when <em>boolean</em> or the acronym for <em>Liver Function Test</em> come up as related words to <em>function</em>. And when the siblings of <em>ritual</em> include <em>sweat lodge</em> and <em>Bat Mitzvah</em> — sure, they're ritualistic activities but not something that a product can inhibit. Defining words is one thing, establishing their intended meaning is another. And when it gets to <em>myth</em>, it's difficult to even find something sensical. </p>

<p>After combing through <a href="https://formeaning.design/definitions/">almost 200 unique definitions and about 1000 synonyms and related words</a>, it's clear that this is not a task to be done manually but to instead train a natural language processing, machine learning algorithm to make connections. Or, simply, to ask people what their understanding of our three selected terms is.</p>

<p>The goal with this first research milestone is to find out if people's meaning of a product changes based on its context of use or if the meaning is inherent in the object and remains largely static across character, context or culture. The next milestone will be to find a way to design for meaning, how to control responses to product aspects.</p>

<p>Csikszentmihalyi and Rochberg-Halton, in The Meaning of Things (1981), in grand terms: </p>

<blockquote>
<p>The objects which people use, despite their incredible diversity and sometimes contradictory usage, appear to be signs on a blueprint that represent the relation of man to himself, to his fellows, and to the universe.</p>
</blockquote>

