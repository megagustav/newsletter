<h5>Curated synonyms from various dictionary sources</h5>

<p><em>Function</em> describes a cognitive objective, <em>ritual</em> a more bodily and social activity and <em>myth</em> describes an emotional state or intellectual value.</p>

<p><em>Myth</em> is complicated. Maybe we should go with something like <em>idea</em> or <em>motivation</em>, but these words imply a cognitive effort whereas <em>myth</em> implies a more stereotypical automation. <em>Symbolism</em> could work, but isn't much more accessible than <em>myth</em>, right? At least it doesn't carry the connotation of deception. </p>

