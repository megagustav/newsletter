<p>Hello! This is the first update on the Design for Meaning Toolkit. You're reading this because you signed up for my work–life updates. If aren't interested now or ever, you may <a href="https://www.youtube.com/watch?v=qMSG4fLMepw">click here to unsubscribe</a>. You'll also find a link in the footer. </p>

<p>Onto the reason for this progress report. We have advanced far enough into our preliminary research to justify a first look back. This helps me reflect on what happened so far, what has proven important, and build paragraphs that I can reuse later for the final report. Does this newsletter make the paper peer-reviewed? Anyway.</p>

<p>In case you missed it, but also to remind myself: Together with my professor and Human Centred Design evangelist <a href="https://www.researchgate.net/profile/Marco_Ajovalasit">Marco Ajovalasit</a> we are exploring Design for Meaning. This will later lead to a toolkit companies can follow to influence the associations users have with their products, and therefore potentially strengthen people's emotional attachment with their possessions. </p>

<p>The first stage of the process is to define the components that give a thing meaning: its function, the ritual of using it, and the inherent symbolism of the object and interactions. We will design a test where people classify artefacts, but before this can happen, we need to find out which words we can use to sort products into categories. We are currently exploring people's definitions of three terms — <em>function</em>, <em>ritual</em> and <em>myth</em>. </p>

<p>Why these words? Research by <a href="https://bura.brunel.ac.uk/bitstream/2438/15652/1/Fulltext.pdf">Joseph Giacomin (2017)</a> suggests that they're terms designers use to define the meaning of their products. Maybe these aren't ideal classifiers but they're a good place to start.</p>

<p>How would you describe the function of the thing you're reading this on? Be it your phone, laptop, or a sheet of paper, these things have different functions, and deliver on those functions with varying levels of satisfaction. <em>Function</em> is still relatively straightforward, while <em>ritual</em> and <em>myth</em> might be too religiously loaded to be clearly interpreted as descriptors for things.</p>

<p>Below is a table with definitions and related words from different dictionaries. We have a desired meaning we're going for when talking about <em>function</em>, <em>ritual</em> and <em>myth</em>. Function is what a product does, how it works, how it responds to interaction, what it's for. Ritual is the act of interaction, we're moving away from the product itself and talk about the user's movements, their frequency and pitch, are they personal or collective acts? Myth is about intent, idea, motivation, about a reason for the product's existence in the user's life, a reason transcending the product's function. </p>
