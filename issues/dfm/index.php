		<h3 id="articles">Notes on Articles <a class="anchoref" href="#articles"><i class="far fa-link"></i></a></h3>

		<h6 id="fn6ref">Crilly, N., Maier A., Clarkson, P. J. (2008). Representing Artefacts as Media: Modelling the Relationship Between Designer Intent and Consumer Experience. International Journal of Design, 2(3), 15-27 <a href="#fn6ref"><i class="far fa-link"></i></a> <a href="#fn6"><i class="far fa-file-alt"></i></a></h6>
		
		<p>There are many different models connecting designers and consumers, yet there are largely disconnected. <a href="https://www.xkcd.com/927/">XKCD Comic about Standards<sup><i class="fas fa-sm fa-external-link"></i></sup></a></p>
		<blockquote><p>Unnecessary effort is expended in developing representations that duplicate those that already exist, [and] new models are developed from inappropriate foundations.</p></blockquote>
		<p>&mdash; we need to make sure that what we're producing deserves to exist and either adds value to established models or disproves them.</p>
		<p>Models: "Model of Design", an encompassing guide for other models from communication to psychology. The artefact can be a medium. <mark>rendering design more intelligible to non-designers</mark>. </p>
		<blockquote><p>This variety of interpretations is relevant not just to the physical products of industrial design, but also to the forms and spaces of architecture, the user interfaces of software applications, and the outputs of many other design activities.</p></blockquote>
		<p>&mdash; Features elicit (sometimes intended) interpretations. Interpretations cannot be controlled because people have different preexisting conditions. Different priming.</p>
		<p>Designers are separated from users. Models exist to connect their worlds but vary in accuracy and efficacy of bridging the cognitive gap. We're looking at words that can be used by both designers and users to communicate and transfer knowledge and understanding of the product. The meaning of the product is highly personal, but is it predictable? Can we build a guide with a universal lexicon, repeatable steps to elicit a meaning for the target consumer, and with a permanent standing in the world of design research?</p>
		<blockquote><p>[The] different diagrams all emphasise the <b>physical, temporal or cultural</b> distances that can separate designers from consumers. The designed artefact will be interpreted without access to the designer, and independently of the original intentions.</p></blockquote>
		<p>&mdash; The product is a communication medium. It says things about itself, about it's usability, about its cultural value, desirability. It needs the ability to talk on its own, nobody will be there to help it.</p>
		<p>Deutsch's (1952) "Structure of Symbols" can provide a canvas for the Design for Meaning Toolkit. Design "models" employ <mark>a structure of symbols and operating rules which is supposed to match a set of relevant points in an existing structure or process</mark> (Deutsch, K. W. (1952). On communication models in the social sciences. The Public Opinion Quarterly, 16(3), 356-380). From a usability perspective, this means that users can interpret and understand aspects of the product step by step <mark>interpret and act upon the representation</mark>, without the cognitive overload of needing to understand all functions at once <mark>rather than being forced to directly understand the full variety and complexity of the structures and processes of interest</mark>. Through this approach, a user can form their own understanding (memories through deep work rather than shallow passive consumption): make connections to preconditioned knowledge and build new understanding</p>
		<p>Shannon's model of communication: information source produces a message. message is encoded into a signal and sent through a channel. receiver decodes signal and reads the message. Similar to encryption. But who holds the keys?</p>
		<p><mark>[Designers] discover and re-discover their intentions during an exploratory creative process</mark>, fine-tuning your message before encrypting it makes the sender also a form of recipient. They receive this message in the iterative process of sketching, prototyping, researching. The message is clarified through iteration, its essence discovered through deep work.</p>
		<p>The product exists within a context of use. This context shapes the decoding process and influences the recipient's interpretation of the message. Note: the recipient doesn't <i>passively consume</i> the message, interaction with the artefact is an active process.</p>
		<p>Mutual Awareness: more pronounced in software, where OS vendors have different models the user needs to learn. One company might have a different signal for a certain message than another. The user needs to be able to decode a same message with different signals without mixing up the decoding process, eg. ⌘+C vs ctrl+C</p>
		<p><mark>Designers must be aware of their audiences' experiences in order to communicate with them</mark> &mdash; we must understand the context of use as well as the existing knowledge of the user. This is where interviews and shadowing help paint us a picture.</p>
		<p><mark>Render the process of design and consumption intelligible to non-designers</mark> &mdash; Don't be elitist. We need to talk to users with everyday language. This will make products more intuitive from a usability perspective but also more their meaning more transparent.</p>
		
		<hr />
		
		<h6 id="fn5ref">Newport, C. (2016). Deep Work: Rules for Focused Success in a Distracted World <a href="#fn5ref"><i class="far fa-link"></i></a> <a href="#fn5"><i class="far fa-file-alt"></i></a></h6>
		
		<blockquote><p>The task of a craftsman it not to generate meaning but rather to cultivate in himself the skill of discerning the meanings that are already there.</p></blockquote>
		<h6 id="fn11ref">Dreyfus, H, and Kelly, S. D. (2011), All Things Shining: Reading the Western Classics to Find Meaning in a Secular Age. New York: Free Press <a href="#fn11ref"><i class="far fa-link"></i></a> <a href="#fn11"><i class="far fa-file-alt"></i></a></h6>
		
		<hr />
		
		<p>Back to Newport: <mark>This frees the craftsman of the nihilism of autonomous individualism, providing an ordered world of meaning.</mark> &mdash; The artefact is a communication medium in a dialogue between designer and user. By gathering data from different cultures and contexts, we can potentially outweigh the Enlightenment's <i>individual</i>. </p>
		<blockquote><p>Another issue muddying the connection between depth and meaning in knowledge work is the cacophony of voices attempting to convince knowledge workers to spend more time engaged in shallow activities.</p>
		<p>Just because this connection between depth and meaning is less clear in knowledge work, however, doesn’t mean that it’s nonexistent.</p>
		<p>It’s clear that by embracing depth over shallowness you can tap the veins of meaning.</p></blockquote>
		<p>&mdash; Here, focus can provide depth and meaning to your work.</p>
		<p><i>c.f.</i> Czikszentmihalyi, M., Larson, R. (1983) Experience Sampling Method (ESM)</p>
		<blockquote><p>Dreyfus and Kelly emphasize, for all its good in the political arena, in the domain of the metaphysical this thinking stripped the world of the order and sacredness essential to creating meaning. In a post-Enlightenment world we have tasked ourselves to identify what’s meaningful and what’s not, an exercise that can seem arbitrary and induce a creeping nihilism. “The Enlightenment’s metaphysical embrace of the autonomous individual leads not just to a boring life,” Dreyfus and Kelly worry; “it leads almost inevitably to a nearly unlivable one.”</p></blockquote>
		<p>&mdash; Place <i>meaning</i> in a justifiable context.</p>
		<p><mark>The goal you pursue needs to resonate at a more human level. Whether you approach the activity of going deep from the perspective of neuroscience, psychology of lofty philosophy, these paths all seem to lead back to a connection between depth and meaning. </mark> Going back to the craftsman: <mark>“The woodworker has an intimate relationship with the wood he works. Its subtle virtues call out to be cultivated and cared for.” In this appreciation for the “subtle virtues” of his medium, they note, the craftsman has stumbled onto something crucial in a post-Enlightenment world: a source of meaning sited outside the individual. The wheelwright doesn’t decide arbitrarily which virtues of the wood he works are valuable and which are not; this value is inherent in the wood and the task it’s meant to perform.</mark> &mdash; We have the power to design products with a certain intent and meaning. Whether this is intent is interpreted as desired by the user depends on the message itself, its context, how well it's encoded, and existing conditions of the user.</p>
		<p><mark>The meaning uncovered by such efforts is due to the skill and appreciation inherent in craftsmanship—not the outcomes of their work. Put another way, a wooden wheel is not noble, but its shaping can be.</mark> &mdash; The wheel isn't special, but <i>this one</i> is.</p>
		<p>Cal Newport's book is about the meaning people find in their work, but the uses of the word can be interesting for the research on Design for Meaning.</p>
		
