<p>Wishes are in order — congratulations or condolences, I don&rsquo;t know. You inspire and influence me. I have been considering a newsletter for a while now. An intimate publication for people who care, nothing out in the open. Something more than a <em>&ldquo;how are you?&rdquo;</em>, <em>&ldquo;good, you?&rdquo;</em>, <em>&ldquo;good&rdquo;</em>. Something universal and yet personal.</p>

<p>I don&rsquo;t want these letters without a name to become too long. If written once a month, they might accumulate baggage, or thoughts might lose their relevance (perhaps a sign they needed their time to expire, perhaps a shame because they could have sparked something). Let&rsquo;s do this every two weeks. I&rsquo;m not sure I can commit to a weekly schedule. My goal is to never skip an issue, but to also have something of substance in each one. The format? Three topics, sometimes two, sometimes one. The topics? Design Thinking, Tech, Food, Project Ideas, Explorations in <span class="smallcaps">UX</span>. This is not a diary. I&rsquo;m not recounting my life or experiences, this is a journal of ideas, of process. Maybe a word limit? I&rsquo;ve barely introduced the scope of this voyage. Onwards.</p>

<p>So today, while introducing this thing (I promise it will have a name soon), let’s do two topics. Keep it light. One is the newsletter itself. The other is related to lever espresso machines.</p>

<hr />

<h3 id="my-first-pony">My first Pony</h3>

<p>I have a few goals for this untitled endeavour, in no particular order.</p>

<ol>
<li>Work on my expression. Differentiation is essential in our field, and at the center stands a solid communication strategy. Having great products is of importance, of course, but if you can&rsquo;t express yourself, the products are worthless. I want to work on linguistic clarification.</li>
<li>Sort my thoughts. Yes, this is another selfish reason to write a newsletter, but maybe you&rsquo;ve had similar ideas to those discussed today and you need a fresh perspective. I know that I need to be writing down my thoughts, or at least giving them time to steep in my head, or message a friend, whatever.</li>
<li>Give ideas time. Related to the above. Maybe you don&rsquo;t give a thought the value it deserves. It&rsquo;s a photo or a line of text, you send it out and it&rsquo;s in the ether, you can remove it from your head. It&rsquo;s alleviating, but maybe it could have gone somewhere else entirely if you&rsquo;d given it a night&rsquo;s sleep. This is also a reason for writing instead of tweeting or making a video — not to argue that any medium is worth more than the other. For me, right now, I need to be writing things down.</li>
<li>Discipline. I want to say I have a good work ethic, but there&rsquo;s room for improvement. A Louis <span class="smallcaps">XVI</span> ballroom for improvement, perhaps. Regularly sitting down to write this would not only allow me to keep my todo list in check, but also give me some beta testers for product analysis.</li>
<li>Collection. Have a medium to gather thoughts, themes, into a text file. Compare, contrast, send it out, get feedback, start a conversation.</li>
<li>Filtering. Writing things down lets to air those old ideas. Some might reveal something beautiful under their layer of dust, others need to thrown out. Perspective allows for better criticism. Cleanly sever dead ideas with a sharp blade of scrutiny. A Louis <span class="smallcaps">XVI</span> guillotine of scrutiny, perhaps.</li>
</ol>

<p>John Siracusa wrote on <a href="http://hypercritical.co/2013/02/08/dont-stop-thinking-about-tomorrow">Hypercritical</a>:</p>

<blockquote>
<p>When it comes to electronics, density is often a signal of quality. A product that feels like an empty metal box seems cheap. A tiny item with surprising heft seems expensive.</p>
</blockquote>

<p>A different density: Saying more with less. This is already going on too long. My favourite books, podcasts, evenings with friends over meals, newsletters even, shouldn’t end. They invariably do. Being finite probably is to their benefit. A simple equation of supply and demand.
How do I write something that you don’t want to put down? Practise. I’m sure you’ve heard this before, but Ira Glass has this poignant quote about the gap between our taste and the quality of our work (<a href="https://vimeo.com/24715531">Vimeo video</a>):</p>

<blockquote>
<p>We get into [creative work] because we have good taste. But there’s like a gap. That for the first couple years that you’re making stuff, what you’re making isn’t that good. It’s trying to be good, it has ambition to be good, but it’s not quite that good. But your taste, the thing that got you into the game, is still killer. And your taste is good enough that you can tell that what you’re making is kind of a disappointment to you.</p>

<p>It’s only by going through a huge volume of work that you are actually going to catch up and close that gap, and the work you’re making will be as good as your ambitions.</p>
</blockquote>

<p>I remind myself of this quote at least twice a year. But what is my motivation to work for the sake of improving my work? I’m not talking about paid work or things with a real deadline. This is self-improvement stuff. I (and maybe others?) need a push. Making a promise to a friend to update them regularly is a good push.</p>

<h3 id="lever-coffee-machines">Lever Coffee Machines</h3>

<figure>
  <img src="https://notes.house/issues/000/elektra-microcasa-a-leva.jpg"/> 
  <figcaption>
    <h5>Elektra Microcasa a Leva</h5>
  </figcaption>
