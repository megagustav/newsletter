<p>I found this gem about the Elektra Micro Casa lever machine <a href="http://coffeegeek.com/proreviews/detailed/microcasaleva/conclusion">in a coffee forum</a>:</p>

<blockquote>
<p>It&rsquo;s beautiful, it&rsquo;s functional, and it&rsquo;s unique. It produces a superior cup of espresso, if you work hard enough at it. What more could you ask for?</p>

<p>A lot, of course. Not everyone wants to put in the kind of time and effort a piston machine absolutely demands of its users. Why bother with this old style technology when state of the art automatics can produce an equally superior shot? Why bother when, in today&rsquo;s technologically advanced world, you can buy a super-auto that can produce an okay shot of espresso without even you thinking about it?</p>

<p>You don&rsquo;t buy a lever machine, or this Micro Casa a Leva if you want life to be easy.
You buy this machine if you have an intense love for espresso, appreciate complete hands-on experience, and are willing to put up with a very, very long learning curve that may require you to re-learn some things you thought you knew.</p>
</blockquote>

<p>I love this idea. I have to admit that I don’t have an intense love for espresso, but I have an intense love for the ritualisation of routines. Be it matcha or espresso, a bath or a shave. You can spend less money to have a better outcome most of the time. But if you want to get somewhere extraordinary, you have to walk the walk. You’ll have an expensive learning curve, but you’ll be wiser for it. As much as I enjoy the “magic” in technology (a perfect piece of glass that does everything), the engineer and process-fan in me enjoys demystification even more. To me, showing the magic makes it even more special. Speaking of Ira Glass, there’s a <a href="https://www.thisamericanlife.org/619/the-magic-show/act-one-8">relevant act</a> from a This American Life show with Teller, of Penn and Teller:</p>

<blockquote>
<p>If you understand a good magic trick — and I mean really understand it, right down to the mechanics at the core of its psychology — the magic trick gets better, not worse.</p>
</blockquote>

<figure>
  <img src="https://notes.house/issues/000/lynweberworkshop-hg-1.jpg"/> 
  <figcaption>
    <h5>Lyn Weber HG-1</h5>
  </figcaption>
</figure>

<p>I discovered <a href="https://lynweber.com/products/">Lyn Weber Workshops</a> today. They make precise coffee grinders and accessories. Every element is thoughtfully considered. Once you familiarise yourself with their <span class="smallcaps">CNC</span>-machined, aircraft grade aluminum amalgamations of engineering and design, you won’t be surprised that they expect you to use callipers to check if their grinding redistribution funnel is compatible with your portafilter.</p>

<p><a href="https://en.wikipedia.org/wiki/Bowellism">Bowellism</a> is a modern architecture style that shows a buildings insides, on the outside. A famous example is the Centre Pompidou in Paris, or the Lloyds Building in London. It’s a different example, but it speaks to the same truth: Design is the differentiator. Smartphones have notches, punched-out holes, reflective backs, ever-increasing camera arrays. But instead of wrapping the same product in a different concealing cloak, Elektra or Lyn Weber reveal their naked functionality: <em>This is how we pull it off.</em></p>

<p>Till next time,<br>
G</p>

