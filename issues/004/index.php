<p>With the intent to broaden my understanding of UX, I set in motion a plan to leave my comfortable job and comforting apartment for something foreign and a little frightening.</p>

<p>The goal of my job at enersis was to apply my knowledge in Human Centered Design to the UX Design process. It took a <a href="https://www.nngroup.com/ux-conference/">NN/g conference</a> in Amsterdam to make me realise not just the parallels between Industrial Design and Interface Design, but rather how indistinguishable the fields of design are once you set aside their medium. It was during that conference that I realised how much more there was to learn in terms of framing a problem &mdash; the research leading to an understanding of the blurry outline of a solution. A first step towards the end product!</p>

<p>Technique, skill, taste can be developed independently from their medium.</p>

