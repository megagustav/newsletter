        <h2 class="article_title_list article_title single">Down the Esoteric Rabbit Hole</h2>
        <h4>March 10th, 2020</h4>
      </div>

<p>To gain deeper insight into UX, I joined <a href="https://www.enersis.ch/?lang=en">enersis</a> in December of 2018. My role was to make human-understandable interfaces for energy supply companies and municipalities to monitor their network, predict measures for improvements and keep track of outages. A little over a year later, I set in motion a plan to leave my comfortable job for something foreign and a little frightening.</p>

<p>The goal at enersis was to apply my knowledge in Human Centered Design to the UX Design process. It took an <a href="https://www.nngroup.com/ux-conference/">NN/g conference</a> in Amsterdam to make me realise how indistinguishable the principles of digital and physical design are once you set aside their medium.</p>

<p>Technique, skill, taste can be developed independently from their medium. A good web developer should be familiar with a range of front-end languages and also know their way around server-side programming, a good carpenter knows the benefits and drawbacks of different tools and wood types and when to use metal components instead, and a good designer should feel at home working with pencil or pixels.</p>

<p>It was during that conference that I realised how much more there was to learn in terms of framing a problem &mdash; the research leading to an understanding of the blurry outline of a solution. A first step towards the end product!</p>

<p>Without understanding a usability problem or human perspective, you will work towards a failed solution. My research used to be mostly retroactive and superficial, based more on intuition. Intuition can be motivating but it’s the scientific method that provides repeatable results.</p>

<p>For the next six months, I’ll be working with <a href="https://www.researchgate.net/profile/Marco_Ajovalasit">Marco Ajovalasit</a> at Politecnico di Milano researching people’s implicit associations with object properties, in the end creating a Design for Meaning toolkit.</p>

<p>In the first stage of the research project, we will dive down the esoteric rabbit hole and define what <i>meaning</i> means. This needs to be defined before we can explore what the characteristics of products mean to people.</p>

<p>I’ll be sharing my progress here.</p>

